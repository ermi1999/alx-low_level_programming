/**
 * add - adds two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - divides twonumbers and processes the reminder
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
